var active_tab = null;

const menuBtn = document.querySelector('.menu-btn');
const menuItems = document.querySelector('.nav-menu-items');
const menuItem = document.querySelectorAll('.menu-item');

// main toggle
menuBtn.addEventListener('click', () => {
  toggle();
});

// toggle on item click if open
menuItem.forEach((item) => {
  item.addEventListener('click', () => {
    if (menuBtn.classList.contains('open')) {
      toggle();
    }
  });
});

function toggle() {
  menuBtn.classList.toggle('open');
  menuItems.classList.toggle('open');
}

// FILTERING CARDS
$('.card-menu button').click(function () {
  active_tab = $(this).data('target') != active_tab ? $(this).data('target') : null;

  $('.card-menu button').each(function (index, button) {
    if ($(button).data('target') == active_tab) {
      $(button).addClass('checked');
    } else {
      $(button).removeClass('checked');
    }
  });

  filterProjects();
});

$(document).ready(function () {
  filterProjects();
});

function filterProjects() {
  let items = projects;

  if (active_tab != null) {
    items = projects.filter(function (project) {
      return project.category == active_tab;
    });
  }

  // Clear <element>
  $('.cards').html('');

  $.each(items, function (index, project) {
    $('.cards').append(
      `
        <div href="#" class="card">
          <div class="card-inner">
            <img src="./assets/img/${project.img}" alt="">
            <div class="card-info">
              <span class="academy">${project.academy}</span>
              <h3 class="title">${project.title}</h3>
              <p>${project.description}</p>
              <p class="date">${project.date}</p>
              <a href="/projects/${project.id}" class="btn">Дознај повеќе</a>
           </div>
         </div>
       </div>
  `
    );
  });
}
